def _lookup(data: dict[str, Any], key: str) -> Any:
    current: Any = data
    for part in key.split("."):
        if not isinstance(current, dict) or part not in current:
            return None
        current = current[part]
    return current


def get_message(key: str, **kwargs: Any) -> str:
    data = load_json_resource("bot_messages.json", DEFAULT_MESSAGES)
    value = _lookup(data, key)
    if value is None:
        value = _lookup(DEFAULT_MESSAGES, key)
        if value is not None:
            backfill_json_resource_value("bot_messages.json", key, value, DEFAULT_MESSAGES)
    if value is None:
        value = key

    text = str(value)
    if not kwargs:
        return text
    try:
        return text.format(**kwargs)
    except Exception:
        return text
